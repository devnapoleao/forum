Página "cadastrar.php":
Exibir formulário de cadastro com campos como "Nome", "Usuário" e "Senha".
Se o usuário pressionar "Cadastrar":
  Validar os dados do formulário.
  Se os dados forem válidos:
    Inserir o novo usuário no banco de dados.
    Redirecionar para "index.php" com uma mensagem de sucesso.
  Senão:
    Exibir mensagens de erro.
