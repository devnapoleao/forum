Página "index.php":
Exibir formulário de login com campos "Usuário" e "Senha".
Se o usuário pressionar "Enviar":
  Verificar se as credenciais são válidas no banco de dados.
  Se as credenciais são válidas:
    Redirecionar para "forum.php".
  Senão:
    Exibir mensagem de erro.
Se o usuário pressionar "Cadastrar":
  Redirecionar para "cadastrar.php".
